namespace RPGBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

class Game
{
    /**
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @ORM\ManyToOne(targetEntity="User", inversedBy="games")
     */
    public $user;

    /**
     * @return in
     */
    public function getId()
    {
        return $this->id;
    }

}